package br.edu.ifgoiano.aluno.henrique.aula4;

public class Ponto2D {
	private double x;
	private double y;
	
	public Ponto2D() {
		this.x = 0;
		this.y = 0;
	}
	
	public Ponto2D(double x1, double y1) {
		this.x = x1;
		this.y = y1;
	}
	
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	
	
	
	

}
